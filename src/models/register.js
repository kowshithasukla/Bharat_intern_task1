const mongoose = require("mongoose");

const registerSchema = new mongoose.Schema({
    fullName: {
        type: String,
        required: true
    },
    username: {
        type: String,
        required: true
    },
    email: {
        type: String,
        required: true,
        unique: true
    },
    phone: {
        type: String,
        required: true
    },
    password: {
        type: String,
        required: true
    },
    confirmPassword: {
        type: String,
        required: true
    },
    declaration: {
        type: Boolean,
        required: true
    }
});

const Register = mongoose.model("Register", registerSchema);

module.exports = Register;   